def gift_sbox(input_nibble):
    # Asegúrate de que sea un número de 4 bits (0–15)
    assert 0 <= input_nibble <= 0xF, "La entrada debe estar entre 0 y 15"

    # Separar bits en una lista [LSB, ..., MSB]
    x = [(input_nibble >> i) & 1 for i in range(4)]  # x[0] = LSB
    
    # Paso 1
    x[1] ^= x[0] & x[2]

    # Paso 2
    t = x[0] ^ (x[1] & x[3])

    # Paso 3
    x[2] ^= t | x[1]

    # Paso 4
    x[0] = x[3] ^ x[2]

    # Paso 5
    x[1] ^= x[0]

    # Paso 6
    x[0] ^= 1  # NOT x[0]

    # Paso 7
    x[2] ^= t & x[1]

    # Paso 8
    x[3] = t

    # Recomponer el valor (MSB a LSB: x[3], x[2], x[1], x[0])
    output = (x[3] << 3) | (x[2] << 2) | (x[1] << 1) | x[0]
    #print(f"Entrada: {input_nibble:04b} -> Bits: {x} -> Salida: {output:04b} ({output})")
    return output

