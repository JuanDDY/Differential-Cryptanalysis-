from galois import GF
GF27 = GF(2**7, irreducible_poly="x^7 + x^3 + x^2 + x + 1")

def wgp_sbox():
    # Construye tabla WGP de 128 valores
    sbox = []
    for x_int in range(128):
        x = GF27(x_int)
        y = x**13
        y1 = y + GF27(1)
        val = y + y1**33 + y1**39 + y1**41 + y1**104
        sbox.append(int(val))
    return sbox

def SB_transform(x):
    # x un entero entre 0 y 127
    bits = [(x >> i) & 1 for i in range(7)]
    def Q(b):
        x0,x1,x2,x3,x4,x5,x6 = b
        return [
            x0 ^ (x2 & x3),
            x1,
            x2,
            x3 ^ (x5 & x6),
            x4,
            x5 ^ (x2 & x4),
            x6
        ]
    def P(b):
        return [b[6], b[3], b[0], b[4], b[2], b[5], b[1]]

    b = bits
    for _ in range(5):
        b = Q(b)
        b = P(b)
    b = Q(b)
    b[0] ^= 1
    b[2] ^= 1
    # Reconstruimos entero
    res = sum((b[i] << i) for i in range(7))
    return res

def sb_sbox():
    return [SB_transform(x) for x in range(128)]

