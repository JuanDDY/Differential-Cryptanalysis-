RCON = [0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]


def _xor_words(a: List[int], b: List[int]) -> List[int]:
    return [x ^ y for x, y in zip(a, b)]


def _rot_word(w: List[int]) -> List[int]:
    return w[1:] + w[:1]


def _sub_word(w: List[int]) -> List[int]:
    return [S_BOX[x] for x in w]


def _mul(a: int, b: int) -> int:
    """Multiplicación en GF(2^8) con polinomio irreducible 0x11B."""
    p = 0
    x = a & 0xFF
    y = b & 0xFF
    for _ in range(8):
        if y & 1:
            p ^= x
        hi = x & 0x80
        x = (x << 1) & 0xFF
        if hi:
            x ^= 0x1B
        y >>= 1
    return p


def _sub_bytes(state: List[int]) -> None:
    for i in range(16):
        state[i] = S_BOX[state[i]]


def _inv_sub_bytes(state: List[int]) -> None:
    for i in range(16):
        state[i] = INV_S_BOX[state[i]]


def _shift_rows(state: List[int]) -> None:
    tmp = state.copy()
    for r in range(4):
        row = [tmp[4 * c + r] for c in range(4)]
        row = row[r:] + row[:r]
        for c in range(4):
            state[4 * c + r] = row[c]


def _inv_shift_rows(state: List[int]) -> None:
    tmp = state.copy()
    for r in range(4):
        row = [tmp[4 * c + r] for c in range(4)]
        row = row[-r:] + row[:-r]
        for c in range(4):
            state[4 * c + r] = row[c]


def _mix_columns(state: List[int]) -> None:
    for c in range(4):
        i = 4 * c
        a0, a1, a2, a3 = state[i], state[i + 1], state[i + 2], state[i + 3]
        state[i] = _mul(a0, 2) ^ _mul(a1, 3) ^ a2 ^ a3
        state[i + 1] = a0 ^ _mul(a1, 2) ^ _mul(a2, 3) ^ a3
        state[i + 2] = a0 ^ a1 ^ _mul(a2, 2) ^ _mul(a3, 3)
        state[i + 3] = _mul(a0, 3) ^ a1 ^ a2 ^ _mul(a3, 2)


def _inv_mix_columns(state: List[int]) -> None:
    for c in range(4):
        i = 4 * c
        a0, a1, a2, a3 = state[i], state[i + 1], state[i + 2], state[i + 3]
        state[i] = _mul(a0, 14) ^ _mul(a1, 11) ^ _mul(a2, 13) ^ _mul(a3, 9)
        state[i + 1] = _mul(a0, 9) ^ _mul(a1, 14) ^ _mul(a2, 11) ^ _mul(a3, 13)
        state[i + 2] = _mul(a0, 13) ^ _mul(a1, 9) ^ _mul(a2, 14) ^ _mul(a3, 11)
        state[i + 3] = _mul(a0, 11) ^ _mul(a1, 13) ^ _mul(a2, 9) ^ _mul(a3, 14)


def _add_round_key(state: List[int], round_key: List[int]) -> None:
    for i in range(16):
        state[i] ^= round_key[i]


def _int_to_state(x: int) -> List[int]:
    return list((x & ((1 << 128) - 1)).to_bytes(16, "big"))


def _state_to_int(state: List[int]) -> int:
    return int.from_bytes(bytes(state), "big")


def expand_key_from_hex(master_key_hex: str) -> List[int]:
    """
    Expande una clave AES-128 (exactamente 32 hex chars) en 11 round keys de 128 bits.
    """
    key_hex = master_key_hex.strip()
    if key_hex.startswith("0x") or key_hex.startswith("0X"):
        key_hex = key_hex[2:]
    if len(key_hex) != 32:
        raise ValueError("AES-128 requiere una clave de exactamente 32 hex chars (128 bits).")

    key_bytes = list(bytes.fromhex(key_hex))

    words: List[List[int]] = [key_bytes[4 * i:4 * i + 4] for i in range(NK)]
    for i in range(NK, NB * (NR + 1)):
        temp = words[i - 1].copy()
        if i % NK == 0:
            temp = _sub_word(_rot_word(temp))
            temp[0] ^= RCON[i // NK]
        words.append(_xor_words(words[i - NK], temp))

    round_keys: List[int] = []
    for r in range(NR + 1):
        rk_bytes = words[4 * r] + words[4 * r + 1] + words[4 * r + 2] + words[4 * r + 3]
        round_keys.append(int.from_bytes(bytes(rk_bytes), "big"))
    return round_keys


def aes_encrypt_block(plain128: int, round_keys: List[int]) -> int:
    """
    Cifra un bloque AES de 128 bits con 11 round keys de 128 bits.
    """
    if len(round_keys) != NR + 1:
        raise ValueError("Se requieren 11 round keys para AES-128.")

    state = _int_to_state(plain128)

    _add_round_key(state, _int_to_state(round_keys[0]))
    for r in range(1, NR):
        _sub_bytes(state)
        _shift_rows(state)
        _mix_columns(state)
        _add_round_key(state, _int_to_state(round_keys[r]))

    _sub_bytes(state)
    _shift_rows(state)
    _add_round_key(state, _int_to_state(round_keys[NR]))
    return _state_to_int(state)


def aes_decrypt_block(cipher128: int, round_keys: List[int]) -> int:
    """
    Descifra un bloque AES de 128 bits con 11 round keys de 128 bits.
    """
    if len(round_keys) != NR + 1:
        raise ValueError("Se requieren 11 round keys para AES-128.")

    state = _int_to_state(cipher128)

    _add_round_key(state, _int_to_state(round_keys[NR]))
    for r in range(NR - 1, 0, -1):
        _inv_shift_rows(state)
        _inv_sub_bytes(state)
        _add_round_key(state, _int_to_state(round_keys[r]))
        _inv_mix_columns(state)

    _inv_shift_rows(state)
    _inv_sub_bytes(state)
    _add_round_key(state, _int_to_state(round_keys[0]))
    return _state_to_int(state)


def encrypt_hex(plain_hex: str, master_key_hex: str) -> str:
    if len(plain_hex) != 32:
        raise ValueError("AES-128 cifra bloques de exactamente 32 hex chars (128 bits).")
    round_keys = expand_key_from_hex(master_key_hex)
    c = aes_encrypt_block(int(plain_hex, 16), round_keys)
    return f"{c:032X}"


def decrypt_hex(cipher_hex: str, master_key_hex: str) -> str:
    if len(cipher_hex) != 32:
        raise ValueError("AES-128 cifra bloques de exactamente 32 hex chars (128 bits).")
    round_keys = expand_key_from_hex(master_key_hex)
    p = aes_decrypt_block(int(cipher_hex, 16), round_keys)
    return f"{p:032X}"


